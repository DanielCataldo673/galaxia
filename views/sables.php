                    uso de la fuerza el cristal es corrompido y el cristal se alimenta del sufrimiento del usuario (esto
                    según los cómics de Darth Vader) de ahí se podrá obtener este característico color de cristal, sin
                    embargo hay que tener en cuenta que si la concentración del usuario oscuro no es absoluta o en otros
                    casos si en pleno proceso el cristal se fragmenta este al momento de colocarlo en su sable de luz
