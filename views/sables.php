                <p class="republica2">Poder, pasión, ira, odio y sufrimiento. Usado en su mayoría por los Señores
                    Oscuros Sith. Al principio existían cristales Kyber de color rojo naturales pero con el tiempo y su
                    uso constante por parte de los Sith, estos se fueron perdiendo hasta que prácticamente ya no quedaba
                    ninguno, sin embargo con el tiempo los Sith encontraron una forma de crear un cristal rojo
                    artificial utilizando cualquier otro color de cristal mediante el uso del poder del Lado Oscuro.</p>

