                <h2 id="verde">Sable de Luz Verde</h2>
                <p class="republica2"> Sabiduría. Usado por aquellos personajes que son muy poderosos en cuanto a los
                    poderes de La Fuerza. Estos son seres estudiosos e interesados en los misterios de la Fuerza.
                    Durante un combate, prefieren usar la Fuerza en vez del sable. En la Orden Jedi, se les denomina
                    cónsules Jedi, y no salían a misiones muy seguido, sino que se quedaban en el templo Jedi
                    estudiando. Personajes destacados como Qui-Gon Jinn, Yoda, Kit Fisto, Luminara Unduli, Ashoka Tano y
                    Luke Skywalker llegaron a poseer este color de sable.
                </p>

